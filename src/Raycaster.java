    public Raycaster(){
        loop = new Thread(this);
        window = new Window(new Dimension(800, 608), "Raycaster", this);
        this.camera = new Camera(new double[]{9.0, 11.0}, map, window);
        this.addKeyListener(camera);
        this.calculator = new Calculator(map, camera, window);
    }

    public synchronized void start(){
        if(isRunning) return;
        loop.start();
        isRunning = true;
    }

    public synchronized void stop(){
        if(!isRunning) return;
        try {
            loop.join();
        } catch (InterruptedException e) {
            e.printStackTrace();
        }
        isRunning = false;
    }

    @Override
    public void run() {
        long lastTime = System.nanoTime();
        double amountOfTicks = 60.0;
        double ns = 1000000000 / amountOfTicks;
        double delta = 0;
        long timer = System.currentTimeMillis();
        int updates = 0;
        int frames = 0;
        while(isRunning){
            long now = System.nanoTime();
            delta += (now - lastTime) / ns;
            lastTime = now;
            while(delta >= 1){
                tick();
                updates++;
                delta--;
            }
            render();
            frames++;
            if(System.currentTimeMillis() - timer > 1000){
                this.frames = frames;
                timer += 1000;
                frames = 0;
                updates = 0;
            }
        }
    }

    public void tick(){
        camera.tick();
    }

    public void render(){
        BufferStrategy bs = this.getBufferStrategy();
        if(bs == null){
            this.createBufferStrategy(3);
            return;
        }
        Graphics g = bs.getDrawGraphics();

        //RENDER HERE

        g.setColor(Color.BLACK);
        g.fillRect(0, 0, window.getFrameDimensions().width, window.getFrameDimensions().height);

        camera.render(g);

        calculator.render(g);

        g.setColor(Color.WHITE);
        g.drawString("DIRECTION: " + camera.getDirection()[0] + ", " + camera.getDirection()[1], 5, 10);
        g.drawString("POSITION: " + camera.getPose()[0] + ", " + camera.getPose()[1], 5, 20);
        g.drawString("FRAMES PER SECOND: " + frames, 5, 30);

        //AND HERE

        bs.show();
        g.dispose();
    }
    
